package it.polito.applied.asti.clan.pojo;

public class StatisticsSinglesInfo {

	private String gender;
	private String age;
	
	
	
	public StatisticsSinglesInfo(String gender, String age) {
		this.gender = gender;
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	
	
}
