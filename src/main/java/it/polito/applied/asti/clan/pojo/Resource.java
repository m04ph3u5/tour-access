package it.polito.applied.asti.clan.pojo;

public class Resource {

	private String path;
	private boolean lock;
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public boolean isLock() {
		return lock;
	}
	public void setLock(boolean lock) {
		this.lock = lock;
	}
	
	
}
