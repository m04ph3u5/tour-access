package it.polito.applied.asti.clan.pojo;

public class InfoEnvironmentSonda {

	private String idSonda;
	private float temp;
	private float humid;
	private int numRilevazioni;
	
	
	public String getIdSonda() {
		return idSonda;
	}
	public void setIdSonda(String idSonda) {
		this.idSonda = idSonda;
	}
	public float getTemp() {
		return temp;
	}
	public void setTemp(float temp) {
		this.temp = temp;
	}
	public float getHumid() {
		return humid;
	}
	public void setHumid(float humid) {
		this.humid = humid;
	}
	public int getNumRilevazioni() {
		return numRilevazioni;
	}
	public void setNumRilevazioni(int numRilevazioni) {
		this.numRilevazioni = numRilevazioni;
	}
	
	
	
}
