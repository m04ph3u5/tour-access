package it.polito.applied.asti.clan.pojo;

public class PoiRank {

	private String idSite;
	private String name;
	private String totAccess;
	
	public String getIdSite() {
		return idSite;
	}
	public void setIdSite(String idSite) {
		this.idSite = idSite;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTotAccess() {
		return totAccess;
	}
	public void setTotAccess(String totAccess) {
		this.totAccess = totAccess;
	}
	
	
}
